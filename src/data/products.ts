export const getProductBySlug = (slug: string): Product | undefined => {
  return products.find(p => p.sku.toLowerCase().replace(/-/g, '-') === slug || p.id === slug);
};

export const getRelatedProducts = (product: Product): Product[] => {
  return products
    .filter(p => p.id !== product.id && (p.aesthetic === product.aesthetic || p.brand === product.brand))
    .slice(0, 4);
};
